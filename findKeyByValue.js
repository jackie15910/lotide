const findKeyByValue = function(object, value) {
  let allKeys = Object.keys(object);
  for (let i = 0; i < allKeys.length; i++) {
    let loopKey = allKeys[i];
    if (object[loopKey] === value) {
      return loopKey;
    }
  }
};

